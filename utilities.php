// quick and dirty debugger
function dump($var, $exit = true): void
{
    echo '<pre>';
    print_r($var);
    echo '</pre>';

    if ($exit) {
        exit;
    }
}

// format zip code
function format_zip($zip): string
{
    if ($zip < 1) {
        return '';
    }
    $short = 5 - strlen($zip);
    for ($i = 0; $i < $short; $i++) {
        $zip = '0' . $zip;
    }
    return substr($zip, 0, 5);
}

